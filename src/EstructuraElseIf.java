
public class EstructuraElseIf {

	public static void main(String[] args) {
		/*
		 * En lugar de utilizar if anidados para multiples comprobaciones, de los valores de las variables se utiliza
		 * la secuencia else/if
		 */
		
		int opcion=6;
		
		if(opcion==4){
			System.out.println("Opcion 4");
		} else if (opcion==5){
			System.out.println("Opcion 5");
		} else if (opcion==6){
			System.out.println("Opcion 6");
		} else if (opcion==7){
			System.out.println("Opcion 7");
		}
		

	}

}
