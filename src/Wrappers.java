
public class Wrappers {

	public static void main(String[] args) 
	{
		Integer numeroUno=2;
		Integer numeroDos=2;
		
		int suma = numeroUno + numeroDos;
		System.out.println(suma);

		numeroDos.compareTo(numeroUno);
		System.out.println(numeroDos.compareTo(numeroUno));
		
		numeroUno.compareTo(numeroDos);
		System.out.println(numeroUno.compareTo(numeroDos));
	}

}
